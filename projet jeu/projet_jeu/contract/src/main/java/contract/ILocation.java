package contract;

/**
 * L'interface ILocation
 */
public interface ILocation {
	
    /**
     * Getter pour la position y
     * @return int
     * La position y
     */
    int getY();
    
    /**
     * Setter pour la position x
     * @param y
     * La position y
     */
    void setY(int y);
    
    /**
     * Getter pour la position X
     * @return int
     * La position x
     */
    int getX();
    
    /**
     * Setter pour la position x
     * @param x
     * La position x
     */
    void setX(int x);

}
